<h3>Adapter</h3>
<h4>Definição:</h4>
<p>Quando se quer prover uma interface de uso para outra classe, que não seja realmente especifica para aquele uso.</p>

<p>No lugar de trocar todos os métodos dessa classe para tal Uso.</p>

<p>O ideal é usar o <a href="https://pt.wikipedia.org/wiki/Adapter" target="_blank">Design Pattern Adapter</a></p>

<p>Onde será uma classe que recebe um objeto e tem as funções implementadas e de um certo padrão e chamará internamente as funções do objeto, ficando transparente para o usuário.</p>

